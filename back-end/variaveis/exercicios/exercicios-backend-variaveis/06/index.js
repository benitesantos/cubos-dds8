// Velocidade Média.

// distancia em metros
let distancia = 200

//tempo em segundos
let tempo = 30

// velocidade

velocidade = distancia / tempo

console.log(`A velocidade é ${velocidade} m/s`)

//velocidade em km/h
velocidadeKm = velocidade * 3.6

console.log(`A velocidade é ${velocidadeKm} km/h`)
